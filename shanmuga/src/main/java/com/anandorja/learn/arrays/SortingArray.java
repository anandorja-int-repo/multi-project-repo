package com.anandorja.learn.arrays;

import java.util.Arrays;
import java.util.Collections;

public class SortingArray {
    int[] marks = {56, 76, 34, 55, 88, 97};
    public int sort(){
        Arrays.sort(marks);
        for (int i:marks);
        return 0;
    }
    public int reverseSort(){
        Arrays.sort(marks);
        for (int i:marks);
        return 1;
    }

}
